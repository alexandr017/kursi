        <h2>
            Как проходит обучение </h2>
        <p>
            Как правило, программы обучения делятся на учебные модули. Каждый учебный модуль состоит из теоретических и практических частей. Такая комбинация позволяет быстро усвоить полученную информацию. По завершению проводится тестирование для проверки уровня закрепления обучающего блока.<br>
        </p>
        <p>
            В онлайн-школах формы обучения могут варьироваться. Но некоторые из самых распространенных форм включают:
        </p>
        <ol class="lc_blue ltp_none c_styled">
            <li>
                <p>
                    <b>Видео-лекции:</b> преподаватель записывает и публикует видео-лекции для студентов.
                </p>
            </li>
            <li>
                <p>
                    <b>Живые вебинары:</b> преподаватель и студенты взаимодействуют в режиме реального времени через интернет.
                </p>
            </li>
            <li>
                <p>
                    <b>Онлайн-дискуссии:</b> студенты и преподаватель взаимодействуют через форумы или чаты.
                </p>
            </li>
            <li>
                <p>
                    <b>Индивидуальные проекты:</b> студенты выполняют индивидуальные проекты под руководством преподавателя.
                </p>
            </li>
            <li>
                <p>
                    <b>Интерактивные задания:</b> студенты выполняют интерактивные задания и получают обратную связь от преподавателя.
                </p>
            </li>
        </ol>
